# 5.提示用户输入他的体重和身高，计算身体质量指数 (BMI)。

weight = float(input("请输入您的体重（kg）: "))
height = float(input("请输入您的身高（m）: "))

bmi = weight / (height ** 2)

print(f"您的BMI指数为: {bmi:.2f}")

if bmi < 18.5:
    print("过轻")
elif 18.5 <= bmi < 24.99:
    print("正常")
elif 25 <= bmi <= 28:
    print("过重")
elif 28 < bmi <= 32:
    print("肥胖")
else:
    print("非常肥胖")


